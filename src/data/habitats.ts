// ---- Helper Functions ----

/**
 * Get a habitat by its ID.
 */
export function getHabitatById(id: number): HabitatData | undefined {
  return habitats.find((h) => h.id === id);
}

/**
 * Get all habitats in a given category.
 */
export function getHabitatsByCategory(category: HabitatData['category']): HabitatData[] {
  return habitats.filter((h) => h.category === category);
}

/**
 * Search habitats by query string. Matches against Korean name, English name,
 * Japanese name, and description (case-insensitive).
 */
export function searchHabitats(query: string): HabitatData[] {
  const q = query.toLowerCase();
  return habitats.filter(
    (h) =>
      h.nameKo.toLowerCase().includes(q) ||
      h.nameEn.toLowerCase().includes(q) ||
      h.nameJa.toLowerCase().includes(q) ||
      h.description.toLowerCase().includes(q)
  );
}

/**
 * Get all habitats that contain a specific Pokemon (by English or Korean name).
 */
export function getHabitatsByPokemon(pokemonName: string): HabitatData[] {
  const q = pokemonName.toLowerCase();
  return habitats.filter(
    (h) =>
      h.pokemon.some((p) => p.toLowerCase().includes(q)) ||
      h.pokemonKo.some((p) => p.toLowerCase().includes(q))
  );
}
