export function getPokemonById(id: string): Pokemon | undefined {
  return pokemonList.find((p) => p.id === id);
}

export function getPokemonByType(type: string): Pokemon[] {
  return pokemonList.filter((p) => p.types.includes(type as Pokemon["types"][number]));
}

export function searchPokemon(query: string): Pokemon[] {
  const q = query.toLowerCase();
  return pokemonList.filter(
    (p) =>
      p.name.toLowerCase().includes(q) ||
      p.nameEn.toLowerCase().includes(q) ||
      p.types.some((t) => t.includes(q))
  );
}
